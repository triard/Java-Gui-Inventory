    @Override
    public void tampildata() {
        String[] kolom = {"ID", "Name", "Alamat", "Telepon"};
        ArrayList<Supplier> list = new Supplier().getAll();
        Object rowData[] = new Object[4];

        jTableSupplier.setModel(new DefaultTableModel(new Object[][]{}, kolom));

        for (int i = 0; i < list.size(); i++) {
            rowData[0] = list.get(i).getIdsupplier();
            rowData[1] = list.get(i).getNama();
            rowData[2] = list.get(i).getAlamat();
            rowData[3] = list.get(i).getTelp();
            ((DefaultTableModel) jTableSupplier.getModel()).addRow(rowData);
        }
    }

    @Override
    public void cari(String keywoard) {
         String[] kolom = {"ID", "Name", "Alamat", "Telepon"};
        ArrayList<Supplier> arrayList = new Supplier().search(keywoard);
        Object rowData[] = new Object[4];

        jTableSupplier.setModel(new DefaultTableModel(new Object[][]{}, kolom));
        for (Supplier supplier : arrayList) {
            rowData[0] = supplier.getIdsupplier();
            rowData[1] = supplier.getNama();
            rowData[2] = supplier.getAlamat();
            rowData[3] = supplier.getTelp();

            ((DefaultTableModel) jTableSupplier.getModel()).addRow(rowData);
        }
    }
    

