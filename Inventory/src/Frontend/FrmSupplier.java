    private void jTableSupplierMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableSupplierMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTableSupplier.getModel();
        int row = jTableSupplier.getSelectedRow();
        Supplier supplier = new Supplier();
        supplier = supplier.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        jTextFieldIdSupplier.setText(String.valueOf(supplier.getIdsupplier()));
        jTextFieldName.setText(String.valueOf(supplier.getNama()));
        jTextFieldTelepon.setText(supplier.getTelp());
        jTextFieldAlamat.setText(supplier.getAlamat());
    }//GEN-LAST:event_jTableSupplierMouseClicked

    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        // TODO add your handling code here:
        Supplier supplier = new Supplier();
        supplier.setIdsupplier(Integer.parseInt(jTextFieldIdSupplier.getText()));
        supplier.setNama(jTextFieldName.getText());
        supplier.setAlamat(jTextFieldAlamat.getText());
        supplier.setTelp(jTextFieldTelepon.getText());
        supplier.save();
        jTextFieldIdSupplier.setText(Integer.toString(supplier.getIdsupplier()));
        tampildata();
    }//GEN-LAST:event_jButtonSaveActionPerformed

    private void jButtonDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDelActionPerformed
        // TODO add your handling code herlx
        DefaultTableModel model = (DefaultTableModel) jTableSupplier.getModel();
        int row = jTableSupplier.getSelectedRow();
        Supplier supplier = new Supplier().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        int selectedOption = JOptionPane.showConfirmDialog(null,
        "Data ini akan dihapus?", "kembali", JOptionPane.YES_NO_OPTION);
        if (selectedOption == JOptionPane.YES_OPTION) {
            supplier.delete();
        }
        kosongankanForm();
        tampildata();
    }//GEN-LAST:event_jButtonDelActionPerformed

