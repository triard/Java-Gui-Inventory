    private void tblKategoriMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKategoriMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tblKategori.getModel();
        int row = tblKategori.getSelectedRow();
        Category kategori = new Category();
        kategori = kategori.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));

        jTextFieldIdKat.setText(String.valueOf(kategori.getIdkategori()));
        jTextFieldNmKat.setText(String.valueOf(kategori.getNama()));
        jTextFieldKet.setText(String.valueOf(kategori.getKeterangan()));
    }//GEN-LAST:event_tblKategoriMouseClicked

